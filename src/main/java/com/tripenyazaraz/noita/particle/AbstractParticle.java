package com.tripenyazaraz.noita.particle;

public abstract class AbstractParticle {

    private int x;

    private int y;

    public AbstractParticle(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return this.x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return this.y;
    }

    public void setY(int y) {
        this.y = y;
    }

}
